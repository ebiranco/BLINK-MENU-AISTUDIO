export const formatCurrency = (amount: number, language: Language): string => {
  if (language === 'fa') {
    return `${amount.toLocaleString('fa-IR')} تومان`;
  }
  // This is a placeholder for non-Persian display, you might want a more robust solution
  const toman = amount;
  return `T ${toman.toLocaleString('en-US')}`;
};

