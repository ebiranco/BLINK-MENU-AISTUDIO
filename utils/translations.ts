export const t = (key: string, language: Language): string => {
  return translations[key]?.[language] || key;
};

export const formatCurrency = (amount: number, language: Language): string => {
    // For Persian, format as Toman.
    if (language === 'fa') {
        const formattedNumber = amount.toLocaleString('fa-IR');
        return `${formattedNumber} تومان`;
    }

    // For English, format with commas, assuming a generic currency.
    return amount.toLocaleString('en-US');
};